fun main() {

    val a=10
    val b=20
    val c=a+b
    println(c)

    println(a-b)
    println(a*b)
    println(a/b)
    println(a%b)

    println("----------Relational-----------")
    println(a>b)
    println(a<b)
    println(a>=b)
    println(a<=b)
    println(a==b)
    println(a!=b)

    println("-------assignment------------")
    var d=1
    d+=10
    println(d)
    d-=20
    println(d)

    d*=5 // d=d*5
    println(d)
    d%=10
    println(d)

    d++
    println(d)
    ++d
    println(d)

    --d
    println(d)

    var e=true
    println(e)
    !e
    println(!e)
    e=!e
    println(e)
    !e
    println(!e)
    println("----------------")
    var f=false
    println(!f)



}